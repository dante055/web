exports.getALlTours = async (req, res) => {
  try {
    const numTours = await Tour.countDocuments();

    // we can pass either the model of query our class, bot will work
    // we don't want to bound this class to the Tour resource/model
    // so we pass query instead of model

    // const apiFeatures = new ApiFeatures(Tour, req.query)
    const apiFeatures = new ApiFeatures(Tour.find(), req.query)
      .filter()
      .sort()
      .limitFields()
      .pagination(numTours);
    const tours = await apiFeatures.query;

    res.status(200).json({
      status: 'sucess',
      result: tours.length,
      data: {
        tours,
      },
    });
  } catch (err) {
    res.status(404).json({
      status: 'fail',
      message: err.message,
    });
  }
};

